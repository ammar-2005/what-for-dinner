const recipes = recipe();
let currentRecipeIndex = 0;
 
 
function displayRecipe(index) {
    const data = recipes[index];
 
 
    document.getElementById("recipe-name").innerText = data.recipeName;    
    document.getElementById("recipe-description").innerText = data.description;
    document.getElementById("recipe-image").src = data.imgUrl;          
    document.getElementById("rating").innerText = data.rating;
    document.getElementById("review-count").innerText = `(${data.reviewCount} reviews)`; 
    document.getElementById("prep-time").innerText = data.prepTime + ' mins';
    document.getElementById("cook-time").innerText = data.cookTime + ' mins';
    document.getElementById("serving").innerText = data.serving + ' people';
    document.getElementById("difficulty-badge").innerText= data.difficulty;
 
 
    document.getElementById("difficulty-item").innerText = data.difficultyItems;
 
  
    const totalTime = data.prepTime + data.cookTime;
    const warningDiv = document.getElementById("time-warning");
    warningDiv.style.display = (totalTime > 45 || data.timeWarning) ? 'block' : 'none';
 
  
    const ingredientsList = document.getElementById("ingredients-list");
    ingredientsList.innerHTML = "";
    data.Ingredients.forEach((ingredient, i) => {
        ingredientsList.innerHTML += `
            <li class="d-flex carousel-item-start">
                <div class="d-flex align-items-center justify-content-center bg-orange-step text-white rounded-circle text-xs fw-bold mt-1 me-3 flex-shrink-0" style="width:24px; height:24px;">
                    ${i + 1}
                </div>
                <span class="text-muted">${ingredient}</span>
            </li>`;
    });
 
  
    const instructionsList = document.getElementById("instructions-list");
    instructionsList.innerHTML = "";
    data.Instructions.forEach((step, i) => {
        instructionsList.innerHTML += `
            <div class="d-flex carousel-item-start align-content-center">
                <div class="d-flex align-items-center justify-content-center bg-orange-icon text-white rounded-3 fs-4 fw-bold me-4 flex-shrink-0" style="width:40px; height:40px;">
                    ${i + 1}
                </div>
                <div class="flex-grow-1 pt-2">
                    <p class="text-muted">${step}</p>
                </div>
            </div>`;
    });
 
    
    document.getElementById("calories-value").innerText= data.nutrition.calories + ' kcal';
    document.getElementById("protein-value").innerText = data.nutrition.protein + 'g';
    document.getElementById("carbohydrates-value").innerText = data.nutrition.Carbohydrates + 'g';  
    document.getElementById("fat-value").innerText = data.nutrition.Fat + 'g';             
    document.getElementById("fiber-value").innerText  = data.nutrition.Fiber + 'g';           
    document.getElementById("sodium-value").innerText = data.nutrition.Sodium + 'mg';  
 
 
    const tipsList = document.getElementById("tips-list");
    tipsList.innerHTML = "";
    data.chefsTips.forEach(tip => {
        tipsList.innerHTML += `
            <div class="bg-orange-subtle border-start border-4 border-warning p-3 rounded-3 mb-3">
                <div class="d-flex align-items-center">
                    <div class="d-flex align-items-center justify-content-center bg-orange-st text-white rounded-circle fs-6 fw-bold me-3 flex-shrink-0" style="width:30px; height:30px;">
                        <i class="fa-solid fa-check text-white"></i>
                    </div>
                    <div>
                        <p class="fw-semibold text-muted fs-6 fs-md-5 p-0 m-0">${tip}</p>
                    </div>
                </div>
            </div>`;
    });
}
 
 
document.addEventListener("DOMContentLoaded", () => {
    displayRecipe(currentRecipeIndex);
 
    document.getElementById("next-recipe-btn").addEventListener("click", () => {
        currentRecipeIndex = (currentRecipeIndex + 1) % recipes.length;
        displayRecipe(currentRecipeIndex);
 
       
        const firstTab = document.querySelector('#pills-tab button[id="pills-home-tab"]');
        if (firstTab) {
            bootstrap.Tab.getOrCreateInstance(firstTab).show();
        }
 
        document.getElementById('hero').scrollIntoView({ behavior: 'smooth' });
    });
});